a = float(input('Введите первый коэффициент: '))
b = float(input('Введите второй коэффициент: '))
c = float(input('Введите третий коэффициент: '))
D = b ** 2 - 4 * a * c
if D > 0:
  D = b ** 2 - 4 * a * c
  x1 = ((-b + D * (1 / 2)) / (2 * a))
  x2 = ((-b - D * (1 / 2)) / (2 * a))
  print(f'Первый корень: {x1}')
  print(f'Второй корень: {x2}')
elif D == 0:
  x = -b / (2 * a)
  print(f'Один корень: {x}')
elif D < 0:
  print('Нет корней!')